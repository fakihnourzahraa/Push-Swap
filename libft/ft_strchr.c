char	*ft_strchr(const char *s, int c)
{
	char	*a;

	a = (char *)s;
	while (*a != '\0')
	{
		if (*a == (char)c)
		{
			return (a);
		}
		a++;
	}
	if (*a == (char)c)
		return (a);
	return (0);
}
