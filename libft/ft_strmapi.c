#include "libft.h"

char	*ft_strmapi(char const	*s, char (*f)(unsigned int, char))
{
	char	*a;
	int		i;

	i = 0;
	if (!s || ! f)
		return (0);
	a = (char *)malloc(ft_strlen((char *)s) + 1);
	if (!a)
		return (0);
	while (s[i])
	{
		a[i] = f(i, s[i]);
		i++;
	}
	a[i] = '\0';
	return (a);
}
