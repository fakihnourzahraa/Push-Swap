char	*ft_strrchr(const char *s, int c)
{
	char	*l;

	l = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
		{
			l = (char *)s;
		}
		s++;
	}
	if (*s == (char)c)
	{
		l = (char *)s;
	}
	return (l);
}
